 class DefaultConstructor {
    int id;
    int salary;
    String name;
     public DefaultConstructor(){
   id=45;
   salary=45000;
   name="Rohit";
}
public static void main(String args[]){
    DefaultConstructor emp=new DefaultConstructor();
   

System.out.println(emp.id);
System.out.println(emp.salary);

}
    
}

